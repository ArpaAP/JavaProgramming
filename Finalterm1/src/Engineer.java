public class Engineer {
    String firstName;
    String lastName;
    int salary;
    int workingYears;

    public Engineer(String firstName, String lastName, int salary, int workingYears) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.salary = salary;
        this.workingYears = workingYears;
    }
}
